
#define COMPARE_COUNTERTIME  (3U)


#include <stdbool.h>
#include <stdint.h>
#include <stdio.h>
#include "app_uart.h"
#include "app_error.h"
#include "nrf.h"
#include "nrf_drv_gpiote.h"
